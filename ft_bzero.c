#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;
	int				i;

	ptr = (unsigned char *)s;
	i = 0;
	while (i < (int)n)
	{
		*ptr = 0;
		ptr++;
		i++;
	}
}
